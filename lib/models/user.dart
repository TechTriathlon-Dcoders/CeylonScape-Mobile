class User {
  int id;
  String firstName;
  String lastName;
  String mobileNumber;
  String email;
  String password;
  String nic;
  String image;

  User({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.mobileNumber,
    required this.email,
    required this.nic,
    required this.password,
    required this.image,
  });
}